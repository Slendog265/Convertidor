package dol;

public class CBK {

	private Double Kilobytes;
	private Double Megabytes;
	
	public CBK(Double kilobytes, Double megabytes) {
		super();
		Kilobytes = kilobytes;
		Megabytes = megabytes;
	}

	public Double getKilobytes() {
		return Kilobytes;
	}

	public void setKilobytes(Double kilobytes) {
		Kilobytes = kilobytes;
	}

	public Double getMegabytes() {
		return Megabytes;
	}

	public void setMegabytes(Double megabytes) {
		Megabytes = megabytes;
	}
	
	
	
	
}
